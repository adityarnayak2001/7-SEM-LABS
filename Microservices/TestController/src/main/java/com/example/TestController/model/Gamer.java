package com.example.TestController.model;

public class Gamer {
    private String name;
    private String gamingAlias;
    private String esportsOrg;
    private double netWorth;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGamingAlias() {
        return gamingAlias;
    }

    public void setGamingAlias(String gamingAlias) {
        this.gamingAlias = gamingAlias;
    }

    public String getEsportsOrg() {
        return esportsOrg;
    }

    public void setEsportsOrg(String esportsOrg) {
        this.esportsOrg = esportsOrg;
    }

    public double getNetWorth() {
        return netWorth;
    }

    public void setNetWorth(double netWorth) {
        this.netWorth = netWorth;
    }

}
